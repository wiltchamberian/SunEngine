        void cmdQuad(GpuVertexDataHandle& handle) {
            //创建对应的顶点数据
            float quadVertices[] = { // vertex attributes for a quad that fills the entire screen in Normalized Device Coordinates.
            // positions   // texCoords
            -1.0f,  1.0f,  0.0f, 1.0f,
            -1.0f, -1.0f,  0.0f, 0.0f,
             1.0f, -1.0f,  1.0f, 0.0f,

            -1.0f,  1.0f,  0.0f, 1.0f,
             1.0f, -1.0f,  1.0f, 0.0f,
             1.0f,  1.0f,  1.0f, 1.0f
            };

            VertexBufferLayout layout;
            layout.push_back({ 0,2,EBasicDataType::FLOAT, false,4 * sizeof(float),0,0 });
            layout.push_back({ 1,2,EBasicDataType::FLOAT,false,4 * sizeof(float),2 * sizeof(float),0 });
            g_graphicsLib->applyGpuVertexData(handle, layout, &quadVertices, 6, 4 * sizeof(float), nullptr, 0, 0, EDrawMode::STATIC_DRAW);
            handle.mode = EPrimitiveType::TRIANGLES;
        }


